package Array;

public class ArrayLoop {
    public static void main(String[] args) {
        int [] myList= {15,73,76,27,878,12,98,98,98,98};
        System.out.println(myList[0]);
        System.out.println(myList[1]);
        System.out.println(myList[2]);
        System.out.println(myList[3]);
        System.out.println(myList[4]);
        System.out.println(myList[5]);
        System.out.println(myList[6]);
        System.out.println(myList[7]);
        System.out.println(myList[8]);
        System.out.println(myList[9]);
        for(int i=0; i< myList.length;i++){
            System.out.println(myList[i]);

        }
    }
}
